void I2C_write(uint8_t byte_send) // WRITE
{
	while (!(I2C->STAR1 & I2C_STAR1_TXE))
	{
	};
	I2C->DATAR = byte_send;
}
void I2C_Start(uint8_t i2c_addr) // Start
{
	i2c_timeout = 0;
	I2C->CTLR1 |= I2C_CTLR1_ACK;
	while (I2C->STAR2 & I2C_STAR2_BUSY)
	{
	}; // I2C_BUSY
	I2C->CTLR1 |= I2C_CTLR1_START;
	while (!(I2C->STAR1 & I2C_STAR1_SB))
	{
	}; // Start - Generated
	I2C->DATAR = i2c_addr;
	while (!(I2C->STAR1 & I2C_STAR1_ADDR) && i2c_timeout < 5000)
	{
		i2c_timeout++;
	}; // ACK - Received

	if (i2c_timeout >= 50000)
	{
		i2c_timeout = 0;
		i2c_ack_stat = I2C_ACK_ERR;
	}
	else
	{
		while (!(I2C->STAR2 & I2C_STAR2_MSL))
		{
		}; // Master mode
		i2c_timeout = 0;
		i2c_ack_stat = I2C_ACK_OK;
	}
}
void I2C_Stop(void)
{ // Stop

	while (!(I2C->STAR1 & I2C_STAR1_TXE))
	{
	}; // TX buf Empty
	while (!(I2C->STAR1 & I2C_STAR1_BTF))
	{
	}; // Byte transfer finished
	I2C->CTLR1 |= I2C_CTLR1_STOP;
	while (I2C->STAR2 & I2C_STAR2_BUSY)
	{
	};
}

void CMD(unsigned char byte)
{
	if (i2c_ack_stat != I2C_ACK_OK)
	{
		I2C->CTLR1 |= I2C_CTLR1_STOP;
		while (I2C->STAR2 & I2C_STAR2_BUSY)
		{
		};
		return;
	}
	I2C_write(COMM);
	I2C_write(byte);
}

void LCD_Init(void)
{
	I2C_Start(LCD_Addr);
	if (i2c_ack_stat != I2C_ACK_OK)
	{
		I2C->CTLR1 |= I2C_CTLR1_STOP;
		while (I2C->STAR2 & I2C_STAR2_BUSY)
		{
		};
		return;
	}
	CMD(0xAE);
	for (unsigned char i = 0; i <= sizeof(init_lcd); i++)
	{
		CMD(init_lcd[i]);
	}

	CMD(0x20);
	I2C_write(0x00);
	CMD(0x21);
	I2C_write(0x00);
	I2C_write(0x7F);
	CMD(0x22);
	I2C_write(0x00);
	I2C_write(0x07);

	I2C_Stop();
}

void LCD_mode(unsigned char mode)
{

	I2C_Start(LCD_Addr);
	if (i2c_ack_stat != I2C_ACK_OK)
	{
		I2C->CTLR1 |= I2C_CTLR1_STOP;
		while (I2C->STAR2 & I2C_STAR2_BUSY)
		{
		};
		return;
	}
	if (mode > 0)
	{
		CMD(0xA7);
	}
	else
	{
		CMD(0xA6);
	}
	I2C_Stop();
}
